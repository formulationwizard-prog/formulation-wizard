/**
 * Look up a supplier by name with a conservative fallback when not in the registry.
 */
export function getSupplierInfo(name: string): SupplierInfo {
  const exact = SUPPLIER_REGISTRY[name];
  if (exact) return exact;
  // Loose match — try to find a known supplier whose name is a prefix of the given name
  // (handles cases like "Cargill (Gerkens Zwart)" → matches "Cargill").
  for (const [key, info] of Object.entries(SUPPLIER_REGISTRY)) {
    if (name.startsWith(key) && key.length > 3) return info;
  }
  return {
    name,
    country: 'Unknown',
    tier: 'specialty',
    certs: [],
    typicalLeadTime: '2-4-weeks',
    moqTier: 'medium',
    priceModifier: 1.0,
    notes: 'Supplier not in registry — defaults applied.',
  };
}
